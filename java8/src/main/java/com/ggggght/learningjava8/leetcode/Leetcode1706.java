  public static int[] findBall(int[][] grid) {
    if (grid[0].length == 1) return new int[] {-1};

    int length = grid[0].length;
    int[] ans = new int[length];
    // ÂàÜ‰∏çÂêåÁöÑÊÉÖÂÜµ
    // Â¶ÇÊûúÊòØÂêëÂè≥ ÂàôÈúÄË¶ÅÁúã Âè≥Ëæπ
    // Â¶ÇÊûúÊòØÂêëÂ∑¶ ÂàôÈúÄË¶ÅÁúã Â∑¶Ëæπ
    for (int i = 0; i < length; i++) {
      // ÁúãÁ¨¨iÂàó
      ans[i] = dfs(grid, 0, i);
    }
    return ans;
  }

  static int dfs(int[][] grid, int line, int column) {
    if (line > grid.length - 1 || column > grid[0].length - 1 || column < 0) return -1;
    // Â∑¶
    if (column + 1 < grid[0].length && grid[line][column] == 1 && grid[line][column + 1] == -1) {
      return -1;
    }
    if (column - 1 >= 0 && grid[line][column] == -1 && grid[line][column - 1] == 1) return -1;

    if (line == grid.length - 1) return grid[line][column] == 1 ? column + 1 : column - 1;

    if (grid[line][column] == 1) {
      return dfs(grid, line + 1, column + 1);
    }

    if (grid[line][column] == -1) {
      return dfs(grid, line + 1, column - 1);
    }

    return -1;
  }
